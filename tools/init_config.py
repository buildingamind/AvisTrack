BLUE   = "\033[94m"
GREEN  = "\033[92m"
YELLOW = "\033[93m"
RED    = "\033[91m"
BOLD   = "\033[1m"
DIM    = "\033[2m"
RESET  = "\033[0m"


def _cli_ask(prompt, default=None, required=True):
    suffix = f" [{default}]" if default else ""
    while True:
        try:
            raw = input(f"  {prompt}{suffix}: ").strip()
        except (EOFError, KeyboardInterrupt):
            print("\n\nAborted."); sys.exit(0)
        value = raw or default
        if value or not required:
            return value or ""
        print(f"  {RED}→ Required.{RESET}")


def _cli_ask_int(prompt, default=None):
    while True:
        raw = _cli_ask(prompt, str(default) if default else None)
        try: return int(raw)
        except ValueError: print(f"  {RED}→ Enter a number.{RESET}")


def _cli_ask_float(prompt, default=None):
    while True:
        raw = _cli_ask(prompt, str(default) if default else None)
        try: return float(raw)
        except ValueError: print(f"  {RED}→ Enter a number.{RESET}")


def _cli_ask_choice(prompt, options, default=None):
    for i, o in enumerate(options, 1):
        m = " (default)" if o == default else ""
        print(f"    {BOLD}{i}{RESET}. {o}{m}")
    while True:
        raw = _cli_ask(prompt, default, required=True)
        if raw in options: return raw
        try:
            idx = int(raw) - 1
            if 0 <= idx < len(options): return options[idx]
        except ValueError: pass
        print(f"  {RED}→ Choose 1-{len(options)}.{RESET}")


def _cli_ask_yes_no(prompt, default=True):
    raw = _cli_ask(f"{prompt} ({'Y/n' if default else 'y/N'})", required=False)
    if not raw: return default
    return raw.lower().startswith("y")


def _cli_ask_path(prompt, default=None, must_exist=False, check_warning=True):
    while True:
        raw = _cli_ask(prompt, default)
        expanded = os.path.expanduser(raw)
        if must_exist and not Path(expanded).exists():
            print(f"  {RED}✗ Does not exist: {expanded}{RESET}"); continue
        if check_warning and not must_exist and not Path(expanded).exists():
            print(f"  {YELLOW}⚠ Does not exist yet: {expanded}{RESET}")
            if not _cli_ask_yes_no("Use it anyway?", True): continue
        return expanded


def _run_cli(prefill_root=None, prefill_name=None, output_path=None):
    print(f"\n{BOLD}╔════════════════════════════════════════════╗")
    print(f"║     AvisTrack — Config Wizard  (CLI)       ║")
    print(f"╚════════════════════════════════════════════╝{RESET}\n")

    cfg = {}

    print(f"{BOLD}{BLUE}── 1/8  Experiment Name ──{RESET}")
    cfg["experiment"] = _cli_ask("Experiment name", default=prefill_name)

    print(f"\n{BOLD}{BLUE}── 2/8  Experiment Root ──{RESET}")
    print("  (Top-level folder for this experiment, e.g. /media/.../Wave2)")
    cfg["root"] = _cli_ask_path("Experiment root path", default=prefill_root, must_exist=False)
    root = cfg["root"]
    detected = _detect_structure(root)
    if detected:
        print(f"  {GREEN}✓ Found: {', '.join(detected.keys())}{RESET}")

    print(f"\n{BOLD}{BLUE}── 3/8  Drive Paths ──{RESET}")
    def _dod(key, fallback): return detected.get(key, f"{root}/{fallback}")
    cfg["raw_videos"]     = _cli_ask_path("raw_videos dir",   _dod("raw_videos", "00_raw_videos"),      check_warning=Path(root).exists())
    cfg["dataset"]        = _cli_ask_path("dataset dir",      _dod("dataset",    "01_Dataset_MOT_Format"), check_warning=False)
    cfg["metadata"]       = _cli_ask_path("metadata dir",     _dod("metadata",   "02_Global_Metadata"),    check_warning=False)
    roi = _find_roi_file(root)
    cfg["roi_file"]       = _cli_ask_path("ROI file",         roi or f"{cfg['metadata']}/camera_rois.json", check_warning=False)
    meta = cfg["metadata"]
    cfg["exclusions"]      = _cli_ask_path("exclusions.json",   f"{meta}/exclusions.json",         check_warning=False)
    cfg["ocr_roi"]         = _cli_ask_path("ocr_roi.json",     f"{meta}/ocr_roi.json",            check_warning=False)
    cfg["time_calibration"] = _cli_ask_path("time_calibration", f"{meta}/time_calibration.json",   check_warning=False)
    cfg["train_manifest"] = _cli_ask_path("train manifest",   f"{meta}/train_data_manifest.txt",  check_warning=False)
    cfg["val_manifest"]   = _cli_ask_path("val manifest",     f"{meta}/val_tuning_manifest.txt",  check_warning=False)
    cfg["test_manifest"]  = _cli_ask_path("test manifest",    f"{meta}/test_golden_manifest.txt", check_warning=False)

    print(f"\n{BOLD}{BLUE}── 4/8  Chamber ──{RESET}")
    cfg["n_subjects"] = _cli_ask_int("Number of subjects", 9)
    cfg["fps"]        = _cli_ask_int("Video FPS", 30)
    cfg["target_w"]   = _cli_ask_int("Inference width", 640)
    cfg["target_h"]   = _cli_ask_int("Inference height", 640)

    print(f"\n{BOLD}{BLUE}── 5/8  Model ──{RESET}")
    cfg["backend"]    = _cli_ask_choice("Backend", ["yolo", "dlc", "vit"], "yolo")
    cfg["model_mode"] = _cli_ask_choice("Mode", ["offline", "realtime"], "offline") \
                        if cfg["backend"] == "yolo" else "offline"
    wts = _find_weights(root)
    if wts:
        print(f"  {GREEN}✓ Found {len(wts)} .pt file(s):{RESET}")
        for i, w in enumerate(wts, 1):
            print(f"    {BOLD}{i}{RESET}. {w.replace(root, '{root}')}")
        ch = _cli_ask(f"Pick 1-{len(wts)}, enter path, or press Enter to skip", required=False)
        if not ch:
            cfg["weights"] = ""
            print(f"  {YELLOW}⚠ Weights skipped (not yet trained){RESET}")
        else:
            try:
                idx = int(ch) - 1
                cfg["weights"] = wts[idx] if 0 <= idx < len(wts) else ch
            except ValueError:
                cfg["weights"] = os.path.expanduser(ch)
    else:
        print(f"  {YELLOW}No .pt files found under {root}/03_Model_Training{RESET}")
        ch = _cli_ask("Weights path (or press Enter to skip)", required=False)
        cfg["weights"] = os.path.expanduser(ch) if ch else ""

    print(f"\n{BOLD}{BLUE}── 6/8  Tracking & Pipeline ──{RESET}")
    cfg["conf_threshold"] = _cli_ask_float("Confidence threshold", 0.2)
    cfg["max_gap_frames"] = _cli_ask_int("Max gap frames", 30)
    cfg["pipeline"] = (["transform"] if _cli_ask_yes_no("Include perspective transform?", True) else []) + ["track"]

    print(f"\n{BOLD}{BLUE}── 7/8  Output ──{RESET}")
    cfg["output_format"] = _cli_ask_choice("Format", ["mot", "parquet"], "mot")
    cfg["output_dir"]    = _cli_ask_path("Output directory", f"{root}/../outputs/{cfg['experiment']}", check_warning=False)

    print(f"\n{BOLD}{BLUE}── 8/8  Time Calibration ──{RESET}")
    print("  (Used by tools/calibrate_time.py to map frame↔burn-in time via OCR)")
    cfg["timezone"]    = _cli_ask("Timezone (IANA)", "America/New_York")
    cfg["time_format"] = _cli_ask("Time format (strptime)", "%I:%M:%S %p")

    # Summary
    print(f"\n{BOLD}{'─'*50}{RESET}")
    for k, v in [("Experiment", cfg["experiment"]), ("Root", cfg["root"]),
                 ("Backend", f"{cfg['backend']} ({cfg['model_mode']})"),
                 ("Weights", cfg["weights"] or "(none — not yet trained)"), ("Pipeline", " → ".join(cfg["pipeline"]))]:
        print(f"  {k:14s}  {v}")

    for w in _validate_config(cfg):
        print(f"  {YELLOW}⚠ {w}{RESET}")

    safe = re.sub(r"[^A-Za-z0-9_\-]", "_", cfg["experiment"]).lower()
    out = Path(_cli_ask("Save config to", output_path or f"configs/{safe}.yaml"))
    if out.exists() and not _cli_ask_yes_no(f"{out} exists. Overwrite?", False):
        print("Aborted."); sys.exit(0)

    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(_generate_yaml(cfg))
    print(f"  {GREEN}✓ Config written to {out}{RESET}")
    print(f"\n{BOLD}Next steps:{RESET}")
    print(f"  python tools/sample_clips.py --config {out} --split train --n 20 --duration 3 ...")
    print(f"  python cli/run_batch.py --config {out}\n")


