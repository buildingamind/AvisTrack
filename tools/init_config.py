STANDARD_DIRS = {
    "00_raw_videos":         "raw_videos",
    "00_Raw_Videos":         "raw_videos",
    "01_Dataset_MOT_Format": "dataset",
    "02_Global_Metadata":    "metadata",
    "03_Model_Training":     "model_training",
}


def _detect_structure(root: str) -> dict[str, str]:
    """Scan root for standard folder names → {config_key: abs_path}."""
    found: dict[str, str] = {}
    root_p = Path(root)
    if not root_p.exists():
        return found
    for child in root_p.iterdir():
        if child.is_dir() and child.name in STANDARD_DIRS:
            found[STANDARD_DIRS[child.name]] = str(child)
    return found


def _find_weights(root: str) -> list[str]:
    """Recursively find .pt weight files under root/03_Model_Training."""
    training_dir = Path(root) / "03_Model_Training"
    if not training_dir.exists():
        return []
    return sorted(str(p) for p in training_dir.rglob("*.pt"))


def _find_roi_file(root: str) -> str | None:
    candidate = Path(root) / "02_Global_Metadata" / "camera_rois.json"
    return str(candidate) if candidate.exists() else None


def _generate_yaml(c: dict) -> str:
    """Build a well-commented YAML string from the collected values."""
    root = c["root"]

    def _rel(path: str) -> str:
        if path.startswith(root):
            return "{root}" + path[len(root):]
        return path

    # Build weights line
    if c.get("weights"):
        weights_line = f'"{_rel(c["weights"])}"'
    else:
        weights_line = "null  # not yet trained"

    # Build pipeline lines
    pipeline_lines = "\n".join(f"  - step: {s}" for s in c["pipeline"])

    # Build model extras (realtime mode)
    model_extra_lines = ""
    if c.get("model_mode") == "realtime":
        model_extra_lines = (
            '  keypoint_name: "center"\n'
            "  kalman_filter:\n"
            "    R: 5.0\n"
            "    Q: 0.001\n"
            "    P: 1000.0\n"
        )

    # Assemble YAML line by line (no textwrap.dedent pitfalls)
    lines = [
        "# ──────────────────────────────────────────────────────────────",
        f"#  AvisTrack Config — {c['experiment']}",
        "#  Generated by: tools/init_config.py",
        "# ──────────────────────────────────────────────────────────────",
        "",
        f'experiment: "{c["experiment"]}"',
        "",
        "# ── Drive / NAS paths ──────────────────────────────────────────",
        "# '{root}' = experiment root; resolved automatically at load time.",
        "drive:",
        f'  root:           "{root}"',
        f'  raw_videos:     "{_rel(c["raw_videos"])}"',
        f'  dataset:        "{_rel(c["dataset"])}"',
        f'  metadata:       "{_rel(c["metadata"])}"',
        f'  roi_file:       "{_rel(c["roi_file"])}"',
        f'  exclusions:     "{_rel(c["exclusions"])}"',
        f'  ocr_roi:        "{_rel(c["ocr_roi"])}"',
        f'  time_calibration: "{_rel(c["time_calibration"])}"',
        f'  train_manifest: "{_rel(c["train_manifest"])}"',
        f'  val_manifest:   "{_rel(c["val_manifest"])}"',
        f'  test_manifest:  "{_rel(c["test_manifest"])}"',
        "",
        "# ── Chamber parameters ─────────────────────────────────────────",
        "chamber:",
        f"  n_subjects:  {c['n_subjects']}",
        f"  fps:         {c['fps']}",
        f"  target_size: [{c['target_w']}, {c['target_h']}]",
        "",
        "# ── Model ──────────────────────────────────────────────────────",
        "model:",
        f'  backend: "{c["backend"]}"',
        f'  mode:    "{c["model_mode"]}"',
        f"  weights: {weights_line}",
    ]

    if model_extra_lines:
        lines.append(model_extra_lines.rstrip())

    lines += [
        "",
        "# ── Tracking ───────────────────────────────────────────────────",
        "tracking:",
        f"  conf_threshold: {c['conf_threshold']}",
        f"  max_gap_frames: {c['max_gap_frames']}",
        "",
        "# ── Pipeline ───────────────────────────────────────────────────",
        "pipeline:",
        pipeline_lines,
        "",
        "# ── Output ─────────────────────────────────────────────────────",
        "output:",
        f'  format: "{c["output_format"]}"',
        f'  dir:    "{_rel(c["output_dir"])}"',
        "",
        "# ── Time calibration ───────────────────────────────────────────",
        "# Used by tools/calibrate_time.py to map frame↔burn-in time via OCR.",
        "time:",
        f'  timezone:    "{c.get("timezone", "America/New_York")}"',
        f'  time_format: "{c.get("time_format", "%I:%M:%S %p")}"',
        "",
    ]

    return "\n".join(lines) + "\n"


def _validate_config(c: dict) -> list[str]:
    """Return a list of warning strings (empty = all good)."""
    warnings: list[str] = []
    root = Path(c["root"])
    if not root.exists():
        warnings.append(f"drive.root does not exist: {root}")
    raw = Path(c["raw_videos"].replace("{root}", c["root"]))
    if root.exists() and not raw.exists():
        warnings.append(f"raw_videos dir not found: {raw}")
    if c.get("weights"):
        w_path = c["weights"].replace("{root}", c["root"])
        if root.exists() and not Path(w_path).exists():
            warnings.append(f"weights file not found: {w_path}")
    roi = c["roi_file"].replace("{root}", c["root"])
    if root.exists() and "transform" in c["pipeline"] and not Path(roi).exists():
        warnings.append(f"roi_file not found (needed by transform step): {roi}")
    return warnings


